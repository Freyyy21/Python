#Print Text
#To display text or output values:
print("Hello World")

#Text in Python must be inside quotes. Can use either " double quotes or ' single quotes:
print("Hello World")
print('Hello World')

#Print without a new line
print("Hello World", end="")
print("Hello World")


#Print Number
#However, unlike text, we don't put numbers inside double quotes:
print(21)
#You can also do math inside the print() function:
print(11 + 21)
#You can combine text and numbers in one output by separating them with a comma:
print("I'am", 20, "years old.")